function showPopup(holiday) {
  // נקה את התוכן הקיים בפופ-אפ
  document.getElementById('popup-text').innerHTML = ''; // נקה את הפסקאות הקודמות



  // הוספת כל סקציה לפופ-אפ
  if (holiday.popupContent.sections && holiday.popupContent.sections.length > 0) {
    holiday.popupContent.sections.forEach(section => {
      let sectionTitle = document.createElement('h3');
      sectionTitle.innerText = section.title;
      document.getElementById('popup-text').appendChild(sectionTitle);

      let sectionContent = document.createElement('p');
      sectionContent.innerText = section.content;
      document.getElementById('popup-text').appendChild(sectionContent);
    });
 // נעילת הגלילה בדף הראשי
 document.body.classList.add('body-no-scroll');
 
  }

  // הוספת תמונה - הנחתי שיש רק תמונה אחת לכל חג
  if (holiday.popupContent.images) {
    document.getElementById('popup-img').src = holiday.popupContent.images;
    document.getElementById('popup-img').style.display = ''; // הצג את התמונה במקרה שהוסתרה בעבר
  } else {
    document.getElementById('popup-img').style.display = 'none'; // אם אין תמונה, הסתר את אלמנט התמונה
  }


  const popup = document.getElementById('popup');
  // הצגת הפופ-אפ
  document.getElementById('popup').style.display = 'flex';

  if (!holiday) return;


}






