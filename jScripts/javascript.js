    // בצע חיפוש כאשר משתמש לוחץ על כפתור החיפוש

    function searchHoliday() {
      const searchInput = document.getElementById('search-input').value.trim();
      const holidaysContainer = document.getElementById('holidays');
      const clearFilterBtn = document.getElementById('clear-filter');
      const gallery = document.querySelector('.gallery');
      const galleryNavigation = document.querySelector('.gallery-navigation');
    
      holidaysContainer.innerHTML = '';
    
      if (searchInput !== "") {
        const filteredHolidays = jsonData.holidays.filter(holiday => 
          holiday.title.toLowerCase().includes(searchInput.toLowerCase())
        );
        gallery.style.display = 'none'; // הצגת הגלריה אם יש תוצאות
        galleryNavigation.style.display = 'none'; // הצגת נווט הגלריה אם יש תוצאות
    
        if (filteredHolidays.length > 0) {
          filteredHolidays.forEach(holiday => {
            const holidayElement = createHolidayElement(holiday);
            holidaysContainer.appendChild(holidayElement);
          });
          clearFilterBtn.style.display = 'inline'; // הצגת כפתור "נקה סינון"
         
        } else {
          holidaysContainer.innerHTML = '<p id="search-message">חג/מועד זה לא נמצא</p>';
          clearFilterBtn.style.display = 'inline'; // הצגת כפתור "נקה סינון" גם אם לא נמצאו תוצאות
      
        }
      } else {
        holidaysContainer.innerHTML = '<p id="search-message">אנא הכנס מילת חיפוש</p>';
        clearFilterBtn.style.display = 'inline'; // הצגת כפתור "נקה סינון" גם אם לא נמצאו תוצאות
        gallery.style.display = 'none'; // הסתרת הגלריה אם החיפוש ריק
        galleryNavigation.style.display = 'none'; // הסתרת נווט הגלריה אם החיפוש ריק
      }
    
      // הגדרת עיצוב הודעת החיפוש
      const searchMessage = document.getElementById('search-message');
      if (searchMessage) {
        searchMessage.style.color = 'red';
        searchMessage.style.fontSize = '18px';
        searchMessage.style.fontWeight = 'bold';
        searchMessage.style.marginRight = "102px";
      }
    }
    
