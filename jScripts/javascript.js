function initGallery() {
  let galleryItems = document.querySelectorAll('.gallery-item');
  let dots = document.querySelectorAll('.dot');

  function showGalleryItem(index) {
      // הסתרת כל התמונות
    // הסתרת כל התמונות
galleryItems.forEach(function(item) {
  item.style.display = 'none';
});
      // הצגת התמונה המתאימה
      galleryItems[index].style.display = 'flex';

      // עדכון הנקודה הפעילה
      dots.forEach(function(dot, idx) {
          if (idx === index) {
              dot.style.backgroundColor = '#3D99DC'; // נקודה פעילה
          } else {
              dot.style.backgroundColor = '#bbb'; // נקודה לא פעילה
          }
      });
  }

  // הוספת מאזיני אירועים לנקודות
  dots.forEach(function(dot, index) {
      dot.addEventListener('click', function() {
          showGalleryItem(index);
      });
  });

  // הצגת התמונה הראשונה בטעינת העמוד
  showGalleryItem(0);
}







function displayHolidays() {
  const holidaysContainer = document.getElementById('holidays');

  jsonData.holidays.forEach(holiday => {
    const holidayElement = createHolidayElement(holiday); // יצירת אלמנט החג באמצעות הפונקציה createHolidayElement
    holidaysContainer.appendChild(holidayElement);
  });
}



function createHolidayElement(holiday) {
  const holidayElement = document.createElement('div');
  holidayElement.classList.add('holiday');

  const imageContainer = document.createElement('div'); // הוספת שורה זו
  imageContainer.classList.add('image-container'); // הוספת שורה זו

  const image = document.createElement('img');
  image.src = holiday.img;
  image.alt = holiday.title;
  image.onclick = () => {
    showPopup(holiday);
  };

  const readMoreText = document.createElement('span');
  readMoreText.classList.add('read-more');
  readMoreText.textContent = 'קרא עוד';

  imageContainer.appendChild(image);
  imageContainer.appendChild(readMoreText);

  holidayElement.appendChild(imageContainer); // הוספת imageContainer ל-holidayElement במקום image

  return holidayElement;
}

